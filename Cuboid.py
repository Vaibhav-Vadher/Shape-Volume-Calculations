# Python program to compute Volume of Cuboid
print("Volume of Cuboid")

# input / assignment
length = 20
breadth = 10
height = 15

# compute / process
volume = length * breadth * height 

# output / results
print("Length =",length,"m")
print("Breadth =",breadth,"m")
print("Height =",height,"m")
print("\nVolume of Cuboid =",volume,"sq.m")
